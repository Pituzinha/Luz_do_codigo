#Crie um programa que receba um número real e imprima a metade e o quadrado desse numero.

numero = float (input ("Digite um número real:"))
print (f"A metade de {numero} é {numero / 2}")
print (f" O quadrado de {numero} é {numero ** 2}")

#Peça ao usuário para inserir um número decimal e exiba o dobro e a raiz quadrada desse número.

import math
num = float( input ("Digite um número decimal:"))
print (f"O dobro de {num} é {num * 2}")
print (f"A raiz quadrada de {num} é {math.sqrt(num)}")
